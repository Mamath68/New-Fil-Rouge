<p> La famille des cuivres regroupe des instruments à vent dont le son est produit grâce
    à l’air qui est à l’intérieur du l’instrument.
    <br>
    Mais également grâce à la vibration des lèvres du musicien jouant sur cet instrument placées sur l’Embouchure,
    contrairement aux instruments du groupe des vent des bois(EX. une flûte).
    <br>
    Instrument dont le son est produit par le souffle et la vibration qui est présente en cause d’une anche.
    <br> Cette dernière est placée au niveau de l’embouchure.
    <br>
<p>Tous les cuivres possèdent une embouchure.
    <br>
    Contrairement à ce que leur nom laisse penser, le point commun des instruments de cette famille n'est pas la
    matière qui les constitue, mais une même technique utilisée par le musicien pour produire le son : la vibration
    des lèvres.
    <br>
    Ainsi, même si la majorité des cuivres modernes sont en laiton :
<ol>
    <li class="new-font">Certains « cuivres » ne sont pas en métal, mais en bois (comme le cor des Alpes ou le didgeridoo), en bois
        recouvert de cuir (cornets à bouquin et serpents), en fibre de verre (certains soubassophones). Les «
        cuivres » rudimentaires peuvent être en os, corne (chophar), ivoire (olifant), terre cuite, coquillage
        (conque), roseau, ou toute matière pouvant présenter l'aspect d'un tube </li>
    <br>
    <li class="new-font">Inversement, certains instruments de musique à vent, fabriqués en métal, voire en alliage de cuivre, ne font
        pas partie des « cuivres » au sens défini précédemment, car le principe d'émission diffère. C'est le cas du
        saxophone, qui appartient à la famille des bois – bien qu'il soit majoritairement composé de laiton, la
        vibration est produite par une anche simple en roseau, et non les lèvres du musicien.</li>
</ol>
</p>
